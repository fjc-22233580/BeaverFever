import greenfoot.*;

/**
 * The BridgeTile class represents a bridge tile in the game.
 */
public class BridgeTile extends Actor {

    /**
     * Constructs a new BridgeTile object.
     * It sets the image of the bridge tile.
     */
    public BridgeTile() {
        
        GreenfootImage image = new GreenfootImage("bridgev1.png");
        setImage(image);
    }
    
}
